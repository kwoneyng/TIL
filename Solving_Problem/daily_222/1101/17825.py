from collections import deque

def game(rs=0,cnt=0):
    global mx
    if cnt == 10:
        mx = max(rs,mx)
        return
    num = data[cnt]
    for i in range(32):
        if vis[i] > 0 :
            nxt = start[i][num]
            if vis[nxt]:
                 continue
            else:
                vis[i] -= 1
                if nxt == 32:
                    game(rs, cnt+1)
                else:
                    vis[nxt] += 1
                    game(rs+start[nxt][0],cnt+1)
                    vis[nxt] -= 1
                vis[i] += 1

